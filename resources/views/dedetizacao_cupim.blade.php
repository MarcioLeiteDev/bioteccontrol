<h4 class="text-success">Biologia e Sistemática</h4>
<p>O chamado “cupim subterrâneo” - Cientificamente classificado por “coptotermis havilandi” da família Rhinotermitidae é uma espécie oriental que foi introduzida no Brasil no século passado havendo registro no Rio de Janeiro em 1923 e em Santos 1934. Sendo a principal espécie em área urbana da região sudeste do Brasil.</p>
<p>Apesar de ser chamado “cupim de solo” ou “cupim subterrâneo”, o ataque deste inseto pode ocorrer a partir da fundação de uma edificação sendo ela casa térrea ou grandes edifícios geralmente através das fissuras e rachaduras vão em busca de celulose, comprometendo todo e qualquer tipo de madeira ou material ligado a solo ou paredes.</p>

<img src="image/ciclo-vida-cupim.png" width="100%" title="ciclo de vida do cupim" alt="ciclo vida cupim"/>

<p><span class="text-danger">Reprodutores Alados futuros “Rei e Rainha”</span> elementos provenientes 
de revoada conhecida pelo publico em geral, principalmente na primavera e no verão 
(no inicio da estação das chuvas) quando as verdadeiras nuvens de cupins 
(então chamados de siriris ou aleluias) voando em torno de pontos luminosos e durante a revoada 
os pares se formam no vôo ou no solo. Já no solo ocorre a perda das asas e o par inicia um 
comportamento chamado de “Tandem”, quando um segue o outro o tocando no final do abdome, com antenas
 e palpos. O casal começa então a procurar um local favorável para iniciar um novo ninho a fundação de uma colônia propriamente dita. Ai estabelecido ocorre à primeira cópula. A rainha passa por um processo chamado fisogastria, onde há grande e impressionante crescimento do abdome podendo chegar até cinco centímetros a fêmea é então chamada de fisogástrica podendo por de oito a doze mil ovos dia.</p>

 <p><span class="text-danger">Reprodutor secundário:</span> Reprodutor que se desenvolve dentro da própria colônia (usando em contraposição ao termo reprodutor primário sendo um alado maduro que permanece na sua colônia perde as asas e se torna funcional macho ou fêmea suplementar que poderá substituir o rei e / ou a rainha em caso de suas mortes ou ainda ocorre junto com o par real primário quando acessam grandes estruturas contendo os chamados caixões perdidos que são espaços vagos contendo restos de obras (formas confeccionadas de madeiras, sarrafos, serragem, sacos de papelão que são usados em construções para o transporte de cimento).</p>

<p>A colônia de <span class="text-danger">coptotermis havilandi</span> pode ser policálica, ou seja, que ocupa vários “ninhos” (estruturas independentes), vários deles desprovidos de reprodutores, sendo chamados “ninhos secundários ou subsidiários”. É freqüente encontrar estes ninhos nas construções, porem não e fácil encontrar o ninho central, com o par de reprodutores primários rei e rainha este aspecto da biologia desta espécie torna ainda mais problemática a sua erradicação do local infestado, pois quando constatado pontos de ataques em grandes estruturas (edifícios) em andares superiores se tomarem medidas de controles focalizados elimina-se os ninhos subsidiários e não o ninho central. Neste caso ocorre rapidamente uma nova infestação</p>

<p><span class="text-danger">Operários - </span> são de morfologia bastante uniforme dentro de cada grupo (por exemplo, dentro de uma subfamília); geralmente constituem a casta mais numerosa. Como o próprio nome indica, é o responsável por todo o trabalho da colônia: construção e reparo do ninho, coleta de alimento, alimentação dos indivíduos de outras castas, alem do cuidado com os ovos, com os jovens e com par real. Nas espécies onde não ocorre à casta dos soldados, eles defendem a colônia.</p>

<p><span class="text-danger">Soldados –</span> morfologicamente bem diferentes dos operários são os responsáveis pela defesa da colônia havendo muitas adaptações para esta função, por exemplo, as mandíbulas podem ser muito desenvolvidas (defesa mecânica), de variadas formas, simétricas ou assimétricas. Podem exibir glândulas especais que produzem substâncias usadas como defesa química, e às vezes ocorrem combinações de defesa mecânica e química.</p>

<p>Os soldados não realizam outras tarefas na colônia e são alimentados pelos operários.</p>

