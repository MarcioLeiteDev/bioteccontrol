    <h3 class="bordinha text-success p-2"><i class="fa-solid fa-ban text-success"></i> Dedetização de Baratas</h3>
    <p>As baratas adaptam-se às mais variadas condições do meio ambiente e transmitem ao ser humano doenças como o tiffo, desinteria, hepatite, alergias, leprose e outras, contaminando alimentos. Uma simples fêmea de barata pode, num só ano, ser responsável pela proliferação de milhares de descendentes.
</p>
<p>Os hábitos alimentares das baratas as colocam em contato direto com alimentos, humanos e utensílios
     de cozinha, deixando para trás um cheiro repugnante, fezes, além de uma grande variedade de 
     organismos, tais como: bactérias, protozoários e outros, contaminando alimentos, utensílios, 
        equipamentos, etc. E dentre as doenças transmitidas por baratas temos o cólera, difteria,
         carbúnculo, tétano, tuberculose, diarréias, toxoplasmose, etc.</p>
<h4>Manjejo Integrado</h4>
<p>O manejo de pragas envolve estratégias integradas de tratamento químico e não-químico, onde as 
    técnicas de monitoramento determinam as necessidades de aplicação de inseticidas e outras medidas 
    corretivas.</p>
<h4>Controle Ambiental</h4>
<p>Os métodos de controle não-químico têm um papel vital na eliminação efetiva das baratas, pois de 
    nada adiantará fazermos uso de inseticidas se não cuidarmos das causas que propiciam a infestação 
    e as condições ideais para a sua proliferação.</p>
<p><strong>Medidas de higiene:</strong> promover limpeza rigorosa das instalações, pois um ambiente com
 comida, água e abrigo está sujeito a grandes infestações. E medidas de limpeza como: lavar o local 
 e recolher o lixo com freqüência, evitar deixar o local com restos de comida durante a noite ou 
 durante um período muito longo, uso de latas de lixo com tampa, manutenção dos terrenos ao redor 
 do local limpos, capinados e sem lixo, enfim toda e qualquer medida que elimine ou reduza a quantidade,
  restos de qualquer tipo de alimento e água.</p>

  <p><strong>Inspeção:</strong> manutenção de inspeções periódicas a fim de assegurar-se que não está 
  existindo infestação ou contaminação por baratas. Esta inspeção poderá ser feita mediante o uso de 
  uma lanterna em frestas e buracos ou usando armadilhas.</p>

  <p><strong>Exclusão:</strong> medidas que visam impedir o acesso das baratas dentro das casas ou do 
  ambiente tais como: o uso de sifões nas pias, conserto das tubulações, vedação de frestas e buracos, 
  como azulejos quebrados, rachaduras, tampa de esgoto, etc.</p>

  <p><strong>Habitat:</strong> promover uma maior ventilação do local, mantendo o local sempre seco e arejado.</p>

