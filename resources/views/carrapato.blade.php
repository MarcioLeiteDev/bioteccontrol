<p>Carrapatos ou carraças são pequenos aracnídeos ectoparasitas hematófagos, responsáveis pela transmissão de inúmeras doenças. Registros fósseis sugerem sua existência há pelo menos 90 milhões de anos, com mais de 800 tipos.</p>
<h4 class="text-success">BIOLOGIA</h4>
<img src="image/carrapato.jpg" width="250" style="float:left; padding:15px;" />
<p>Carrapato é o nome popular do artrópode da subclasse Acarina (ácaros), da ordem Ixodida, podendo ser classificado nas famílias Nuttalliellidae, Ixodidae ou Argasidae, tendo a primeira apenas uma espécie e as duas últimas, aproximadamente 700 e 200 espécies respectivamente.</p>
<p>Encontra-se difundido por toda a Terra tanto no campo como na cidade. O principal motivo de sua ação é o ser humano ou animal de cujo sangue se alimenta, sendo por isso considerado hematófago e um dos principais vetores[2] de vírus, bactérias (especialmente as riquétsias[3][4]) e protozoários, que transmitem doenças ao homem e animais.</p>
<p>Existem espécies a partir de 0,25 mm de diâmetro. Vivem em touceiras, capim, no chão,em climas úmidos ou secos.</p>
<p>Os carrapatos geralmente têm a forma oval e quando em jejum são planos no sentido dorso-ventral, porém após se alimentarem ficam convexos e até esféricos.</p>
<p>Sua carapaça é composta por quitina, na forma de um exoesqueleto, bem resistente e firme em relação a sua pouca espessura.</p>

<h4 class="text-success">PREJUIZOS</h4>
<p>Os carrapatos causam grandes prejuízos devido à sua eficiência reprodutiva, pois seu ciclo se completa em 21 dias e cada fêmea põe em media 3000 ovos. Por isso qualquer dano que o carrapato cometa toma enormes dimensões. Este ácaro leva a um atraso no desenvolvimento, enfraquecimento pela perda de sangue, irritação do animal parasitado e desvalorização do couro. Observa-se, também, queda na produção de leite e na engorda dos animais. O carrapato também é um agente transmissor de doenças, entre elas a mais importante é a tristeza parasitária bovina, responsável por perdas econômicas significativas.</p>


<h4 class="text-success">CUIDADOS PESSOAIS</h4>
<p>Para evitar possibilidade de contaminação pela Febre Maculosa Bovina alguns cuidados devem ser tomados visando reduzir a possibilidade de picada e fixação dos carrapatos nos humanos:</p>
<ul>
    <li> Uso de roupas claras, camisa de manga comprida e botas de cano longo com a proteção de fita adesiva entre a calça e a bota.</li>
    <li>Vistoriar o corpo e retirar os carrapatos imediatamente após terminar a atividade de campo.</li>
    <li>Matar os carrapatos com fogo, água fervente ou álcool e não esmagar entre as unhas para não correr o risco de contaminação. Para retirar os carrapatos da roupa pode ser usada fita adesiva e, em seguida, ferver as roupas antes de lavar.</li>

</ul>
<p>Se dias após o contato com carrapatos aparecerem sintomas como gripe forte (febre, desânimo, dores no corpo), falta de apetite ou manchas na pele, deve-se procurar um médico imediatamente e informar sobre o contato com carrapato. É importante lembrar que as larvas e ninfas são os principais responsáveis pela transmissão da febre maculosa brasileira.</p>
