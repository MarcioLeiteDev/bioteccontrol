    <h3 class="text-success bordinha">MÉTODOS DE CONTROLE</h3>
    <h4>Educativo</h4>
    <p>Baseia-se na orientação da população das cidades, alertando-a para que evite alimentar os pombos, pois tal hábito acarreta aumento exagerado do número de aves, com maior risco de transmissão de doenças e danos ambientais. Recomenda-se também evitar deixar restos de alimentos à disposição das aves, bem como manter o lixo acondicionado em sacos plásticos bem fechados. Essas medidas favorecem o controle do número de pombos: a diminuição de alimentos acarreta um menor número de ovos e filhotes.
    </p>
    <h4>Barreira Fisica</h4>
    <p>Este método baseia-se na utilização de telas, fechamento das aberturas por onde as aves adentram, com alvenaria ou outro material resistente; colocação de fios de nylon de aproximadamente 10 cm da base e presos nas extremidades por um parafuso; uso de pontas de arame em locais altos onde não haja acesso de pessoas; mudança do ângulo de inclinação da superfície de apoio das aves para 60 graus, entre outros.
    </p>
    <h4>Repelentes</h4>
