<p>Muitos acidentes por envenenamento são provocados pelos escorpiões, artrópodes pertencentes à classe Arachnida, da ordem Scorpionida. Cerca de 116 gêneros de escorpiões já foram identificados no mundo, os quais subdividem-se em cerca de 1.500 espécies. Embora sejam venenosos e peçonhentos, nem todas as espécies representam riscos a saúde pública. A picada é muito dolorida, realizada através do ferrão que se localiza em sua extremidade final.
</p>
<p>Os escorpiões habitam áreas de clima quente, nas zonas tropicais e subtropicais, além de regiões áridas com muito pedregulho, que proporcionam esconderijo e proteção. Seu alimento preferido são as baratas, mas os grilos e as aranhas também são apreciados. Preferem caçar seu alimento do que recebê-lo morto, mas possuem uma particularidade importante: podem viver até um ano sem se alimentar. Desta forma, adaptam-se facilmente ‘as condições adversas e conseguem sobreviver em esgotos, entulhos, pilhas de madeiras, pilhas de tijolos ou de telhas, cemitérios, abrigos nos domicílios, caixas de fiação elétrica, conduítes, entre outros.</p>
<p>Os escorpiões apresentam uma longevidade que varia entre três e quatro anos, período durante o qual geram de 15 a 30 filhotes. O Tityus serrulatus, o conhecido escorpião amarelo, se reproduz por partenogênese (não necessita do macho para procriar).</p>
<p>O veneno do escorpião pode matar crianças e idosos que estiverem com a saúde debilitada e o tratamento deve ser feito com soro anti-escorpiônico. No Brasil, existem três espécies de maior importância: Tityus serrulatus (o escorpião amarelo), Tityus bahienses e Tityus stigmurus.</p>
<p>Como possuem hábitos noturnos são mais facilmente observados à noite e, para localizá-los, a melhor maneira é fazer uso de uma lanterna de luz ultravioleta, que se reflete nos escorpiões e os torna visíveis, facilitando sua perseguição e captura.</p>

